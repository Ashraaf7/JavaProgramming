package Basics;

public class WhileLoop { //pascal


    public static void main(String[] args) {
       /*
      While Loop : as for :
            1-Start
            2-Condition
            3-Increment or decrement value
        */
        //Print numbers from 1 > 100
        int i = 1 ; //Start
        while ( i <= 100 ) //Condition
        {
            System.out.println(i);
            i++ ;  //Increment
        }
        }

    }



