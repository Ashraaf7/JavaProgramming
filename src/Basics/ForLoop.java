package Basics;

public class ForLoop { //pascal


    public static void main(String[] args) {
       /*
       Loops : is repeating piece of code based on 3 Things :
            1-Start
            2-Condition
            3-Increment or decrement value
        */
        //Print numbers from 1 > 100
        for (int i =0 ; i <= 100 ; i++)
            System.out.println(i);
        }

    }



