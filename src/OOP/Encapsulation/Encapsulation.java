package OOP.Encapsulation;

public class Encapsulation {
    private int user ;
    private int pass ;
    public  Encapsulation()
    {}
    public Encapsulation(int u, int p) {
        this.user = user;
        this.pass = pass;
    }

    public int getUser() {
        return user;
    }

    public void setUser(int user) {
        this.user = user;
    }

    public int getPass() {
        return pass;
    }

    public void setPass(int pass) {
        this.pass = pass;
    }
}
